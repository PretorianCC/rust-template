use std::cmp::PartialEq;

fn main() {
    
    // Сравнение на равенство. (==)
    println!("eq {}", 1.eq(&1)); // eq true
    println!("oprerator == {}", 1 == 1); // oprerator == true

    // Сравнение на не равенство. (!=)
    println!("ne {}", 1.ne(&1)); // ne false
    println!("oprerator != {}", 1 != 1); // oprerator != false

}
